/**
 * Returns a per-event hint about the upcoming patrol event.
 *
 * @param eventId  The pre-seeded patrol event ID, or `null` if none seeded yet.
 */
export function getHint(eventId: string | null): string {
  if (!eventId) return DEFAULT_HINT;

  // Per-event hint lookup (primary)
  const perEvent = HINT_BY_EVENT_ID[eventId];
  if (perEvent) return perEvent;

  // Fallback: look up hintCategory from the event pool and use a generic line
  const v2Event = PATROL_EVENTS_V2.find((e) => e.id === eventId);
  if (v2Event?.hintCategory) {
    return CATEGORY_FALLBACK[v2Event.hintCategory] ?? DEFAULT_HINT;
  }

  return DEFAULT_HINT;
}

